					<p class="p">
						Первым учителем Кевина в айкидо стал сэнсэй Сигэру Судзуки. Огромное влияние оказали на Кевина
						Мицуги Саотэмэ–сэнсэй и Хироси Икэда-сэнсэй. Уже в 1981 г. Кевин стал старшим преподавателем в
						«Чикаго Айкикай». К себе был очень строг, относился к себе по гамбургскому счёту. К другим был
						снисходительней. У Кевина был шестой дан. К данам и другим чинам относился со скрытой усмешкой.
						Но уважал человека. И уважали его. Айкидо для него было постоянным поиском, экспериментом. Он не
						боялся неудач. Он постоянно менялся, не боялся ошибок, не изображал себя всё постигшим мудрецом.
						Не боялся признаваться в собственных ошибках, наоборот, заострял на них внимание. На татами
						держался просто, но с достоинством. Мог он быть и очень строгим, но и тогда в нём не было
						барской заносчивости.
					</p>
					<p class="p">
						Обычно я видел его с перерывами в год, и каждый раз он был не похож на себя предыдущего.
						Понимали ли его те, кто был рядом с ним на татами? И да и нет. Каждый по-своему. Кажется, он
						далеко вперёд ушёл по этой дороге, и ему приходилось оборачиваться, останавливаться,
						возвращаться обратно к тем, кто шёл сзади. Я спросил его, что он думает, почему многие уходят из
						айкидо. Он ответил: «Люди устают от айкидо» Так и сказал: «People get tired doing aikido».
						Прошлой весной в Москве мы разговаривали на кухне, и он сказал, что никто не любит перемен.
						Люди, особенно старшие, комфортно чувствуют себя на насиженных местах. Когда ты затеваешь
						перемены даже в технике, сказал он, будь готов, что мало кто пойдёт вместе с тобой. Я счастлив,
						что судьба свела меня с эти человеком. С ним было просто и здорово. В общении с ним не
						требовалась фальшь. Я не хочу рисовать портрет махатмы, но в нём была черта, редкая в людях. Он
						не закрывался от жизни занавеской и не отмахивался от неудобной правды. Был готов на авантюры,
						любил выпить, его легко было подбить на приключения. Но на занятиях всегда выглядел, как
						подобает учителю. Он был читающим человеком, - редкость в наши дни не только для американца.
						Цитировал Элиота, Шекспира, Донна. Он казался чуть-чуть не от мира сего. Жил очень скромно.
						Личный успех и слава его, кажется, мало интересовали. Его занимал поиск. И он всегда приглашал
						своего ближнего разделить с ним этот поиск и находки. Многим и очень многим, он показал Путь
						Айки, а кого-то и поставил на Путь.
					</p>
					<p class="p">
						Кевин был из вымирающей породы людей. Он умел и понимал. При этом я не видел в нём ни тени спеси
						или самодовольства. Думаю, ему было стыдно за мир, за то, что происходит вокруг. Он не говорил
						об этом сам, и как часто бывает, скрывался за маской веселья. Но и веселье его было с налётом
						грусти. Особенно грустным он показался мне при нашей последней встрече за месяц с небольшим до
						его ухода. Он был обречён так же, как в своё время был обречён доктор Живаго. Он был «как бы» не
						от мира сего.
					</p>
					<p class="p">
						Последнее, что Кевин сказал мне, было: «Он звонит по тебе».
					</p>
					<p class="p">
						Он ушёл в ночь с 1 на 2 декабря 2012 года. Пришедший утром брат не застал его в живых.
					</p>
					<p class="p">
						У него был Путь. Он ушёл. Кевин был человек Пути.
