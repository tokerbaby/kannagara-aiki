					<h2 class="h2">Кевин Шоат. Первая встреча</h2>
					<p class="p">
						О том, что Кевина больше нет, я узнал по телефону. Как часто бывает, понимание пришло не сразу.
						Да и сейчас оно не пришло. Я часто обращаюсь к ушедшим, как будто
						они где-то недалеко и вот-вот откликнутся. Душа не хочет соглашаться с утратой, и ум не
						принимает новой расстановки вещей. Я поймал себя на том, что собираюсь тут же
						написать ему письмо и попенять на случившееся. Какая-то часть меня говорила, что ответа не
						будет, а другая думала, что именно он напишет в ответ. Разумеется, письмо я не
						отправил. Может быть, зря.
					</p>
					<p class="p">
						Впервые я увидел Кевина на большом семинаре в Америке в 1991 году, но настоящее наше знакомство
						состоялось в двухтысячном и сразу стало очень бурным. Мы принимали Кевина и группу его учеников
						в Москве. Это произошло довольно неожиданно. Гости ехали по приглашению в Киев, но сначала
						собирались посмотреть Питер. Путь их лежал через Москву. По плану я должен был встретить их в
						Шереметьево, привезти домой, накормить и отправить в Питер тем же вечером, а уже через несколько
						дней встретить и расселить. Семинар в Москве ожидался после возвращения гостей из Питера. Но
						первая приветственная была опрокинута ещё в аэропорту. Когда же мы добрались до дома, я занялся
						обедом, а Крэг, организатор поездки всей группы гостей, который до этого долго жил в Москве и
						хорошо знал моего семилетнего сына, мне не говоря, занял у него все деньги и отправился в
						ближайший гастроном. Словом, вечер разворачивался. Стали подтягиваться ребята из додзё. Как-то
						сразу все подружились, и всё понеслось-поехало. Комната маленькая, гостей много, как обычно,
						столом служила снятая дверь. И вот, встаёт Кевин, произносит очередную здравицу и говорит: «А
						давайте в Питер не поедем, раз тут так хорошо!» Согласие было получено мгновенно. Я
						приозадачился, потому что совсем не был готов принимать гостей в тот день, а рассчитывал
						приготовится к их возвращению. Но всё вышло славно и само собой. Был конец мая, стояла почти
						жара, Обед перетек в ужин, кто-то предложил вечерний обзор Москвы. Не долго думая, собрались и
						отправились на Воробьёвы горы. Там кто-то спихнул Кевина в Москву реку с невысокой набережной, а
						потом сразу все попрыгали в реку сами, кто в чём был. Кевин потом уверял меня, что он сам
						оступился, но кто теперь разберёт! Была уже ночь. Купание продолжалось до самого утра. Вернулись
						домой уже хорошо засветло. Кевину, как сэнсэю досталась отдельная комната, остальные устроились,
						кто где, а утром я обнаружил несколько тел прямо на полу. Правда, было очень тепло. Потом,
						конечно, всё утряслось, гостей разобрали по домам, немного пришли в себя. Не то, чтобы « всё
						утихло, улеглось», но «обрело свой вид». И через день начался
						семинар.
					</p>
