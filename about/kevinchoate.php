						почти ничего не говорил, зато давал почувствовать. На татами Кевин был чарующе таинствен. При
						этом был удивительно прост в общении, не городил себе пьедесталов величия. Семинар прошёл очень
						здорово. Только спали мы каждую ночь всё меньше и меньше. Последнюю, кажется, два часа. Но сил
						было много, энтузиазма – хоть отбавляй, а гости, почти все первый раз в Москве, хотели увидеть
						побольше. Вот только в Кремль так и не попали. Сначала был выходной в Кремле, потом ещё что-то,
						а потом нагрянул Билл Клинтон, и Кремль вовсе закрыли для посетителей. Прямо «Москва – Петушки».
						Кстати, когда после семинара мы провожали на вокзале гостей в Киев, сообщили, что Клинтон
