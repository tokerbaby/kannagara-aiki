						о том, что для продления собственной драгоценной жизни нужно пресечь чью-то иную. Было уже
						поздно. Наутро я собирался в Москву, а он должен был продолжать семинар.
					</p>
					<p class="p">
						Мне казалось, что ему как-то неуютно быть тем, в чью честь дают банкет. Его уже увозили. Он
