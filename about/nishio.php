						с мечом отразились на его видении айкидо.
						Нисио-сэнсэй считал, что подлинное понимание айкидо может прийти только через опыт обращения с
						оружием, с мечом (кэн) и палкой (дзё). Особое значение сэнсэй уделял упражнениям с настоящим
