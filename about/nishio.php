					<h2 class="h2">Поль Мюллер о Нисио-сэнсэе</h2>
					<p class="p">
						Моими сегодняшними навыками в айкидо более всего я обязан Сёдзи Нисио-сэнсэю, легендарному
						мастеру боевых искусств, прямому ученику О-сэнсэя. Я увидел его впервые в 1986 году, с тех пор
						стал его учеником и учился у него до его последних дней.
						<br><br>
						Сэнсэй покинул нас 15 марта 2005 года в возрасте 77 лет. Он был редким мастером, и одновременно
						отзывчивым и простым в общении человеком. Он также был великолепный психолог, прекрасно
						чувствовал собеседника. Авторитет его распространялся не только на иностранцев. Как мастера и
						гения будо его почитали сами соотечественники.
						Выдающимся мастером Нисио-сэнсэй был не только в айкидо. За ним шла слава мастера меча. Он
						создал свой стиль иайдо, который был официально признан Федерацией Иайдо Японии. Этот стиль
						получил имя Айкидо Тохо Иай.
						С давних пор Нисио-сэнсэй был обладателем восьмого дана Айкидо Айкикай, восьмого дана в иайдо,
						седьмого в каратэ и пятого в дзюдо.
						Нисио-сэнсэй преподавал в Хомбу Додзё при жизни основателя, О-сэнсэя, т.е. до 1969 года.
						Остались вышедшие из-под кисти Нисио-сэнсэя великолепные каллиграфии.
					</p>
					<h3 class="h3">О «Нисио рю», стиле Нисио</h3>
					<p class="p">
						Путь Нисио-сэнсэя в будо был своеобразен. И это отразилось на его стиле, который отличается
						динамичностью, остротой, естественностью и эффективностью. Атэми – его визитная карточка. Момент
						схождения, дэай, и момент входа, ирими, сопровождались атэми, и атэми эти были очень
						разнообразны. Он часто использовал атэми и при развитии приёма во время движения. Арсенал ударов
						был велик. Использовались удары руками, локтями, ногами, коленями. Нисио-сэнсэй считал, что в
						каждом приёме айкидо заложены четыре – пять возможностей нанесения противнику полного поражения.
						При этом он призывал быть осторожнее с применением ударов на практике и не наносить их без
						крайней необходимости. Он напоминал наставления О-сэнсэя о том, что айкидо не направлено на
						уничтожение противника, а наоборот, обязывает нас к бережному отношению к его жизни.
					</p>
					<p class="p">
